    {
        $attributes = request()->validate([
            'first_name'=>'required',
            'last_name'=>'required'
        ]);
        $attributes['middle_name'] = $request->get('middle_name');
        Author::create($attributes);
        $author= Author::latest()->first();
        return response()->json($author);
    }

